        </ul>
        <p>L'applicazione nasce per la gestione di un prodotto open-source ed open-hardware offrendo alcune funzionalit&agrave; come: <br>
            
        </p>
            <ol>
                <li>Registrazione di nuovi prodotti </li>
                <li>Modifica dati utente</li>
                <li>Visualizzazione dei prodotti di cui si conosce l'identificativo</li>
                <li>Visualizzazione dei dati dell'utente.</li>
            </ol>
        <p>
            L'applicazione ha due ruoli: <b> utente </b> e <b> sviluppatore</b>.
        </p>
        
        <p>
            Il ruolo <b>utente</b> consiste nella semplice possibilità di visualizzare alcune informazioni, in particolare:<br>
        </p>
       
            <ol>
                <li>Pu&ograve;visualizzare i propri dati utente</li>
                <li>modificare alcuni dati utente</li>
                <li>visualizzare il codice pubblicato dallo svilupparote</li>
            </ol>
        
        <p>
            Il ruolo <b>sviluppatore</b> oltre alle funzionalità dell'utente base ha anche la possibilità di caricare dei prodotti nuovi<br>
        </p>
        
        <br>
        <p>
            &Egrave; possibile cambiare ruolo attraverso il logout ed il login con il nuovo ruolo. 
        </p>
        <br>
        
        <ul>
            <li><h2><strong> Elenco di quali requisiti soddisfatti </strong></h2></li>
        </ul>
        
            <ol>
                <li>Utilizzo di HTML e CSS (HTML presente in molte pagine php (cartella php), in particolare quelle della view; CSS presente nella cartella css/thema.css)</li>
                <li>Utilizzo di PHP e MySQL (PHP presente in molte pagine php (cartella php); MySQL presente in molte pagine php (cartella php)</li>
                <li>Utilizzo del pattern MVC (le cartelle all'interno del progetto sono suddivise in "model-view-controller")</li>
                <li>Due ruoli: user e developer</li>
